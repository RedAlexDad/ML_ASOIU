plt.show()

def remove_outliers_iqr(dataframe, column):
    Q1 = dataframe[column].quantile(0.25)
    Q3 = dataframe[column].quantile(0.75)
    IQR = Q3 - Q1
    lower_bound = Q1 - 1.5 * IQR
    upper_bound = Q3 + 1.5 * IQR
    outliers = dataframe[(dataframe[column] < lower_bound) | (dataframe[column] > upper_bound)]
    cleaned = dataframe[(dataframe[column] >= lower_bound) & (dataframe[column] <= upper_bound)]
    print(f"{column}: выбросов = {len(outliers)} из {len(dataframe)} ({len(outliers)/len(dataframe)*100:.1f}%)")
    print(f"  Границы: [{lower_bound:.2f}, {upper_bound:.2f}]")
    return cleaned

print("Обнаружение выбросов (метод IQR):")
df_clean = remove_outliers_iqr(df, 'Revenue')
df_clean = remove_outliers_iqr(df_clean, 'Profit')

