axes[1].set_ylabel('Частота')
axes[1].grid(axis='y', alpha=0.3)

plt.tight_layout()
plt.savefig(os.path.join(PLOTS_DIR, 'maxabs_scaling.png'), dpi=150, bbox_inches='tight')
